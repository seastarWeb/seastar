<h1>Accessibility Statement</h1>
<p>
We have made every effort to make this site accessible and easy to use for everyone.

All pages on this site should be valid to HTML 5 - the site also uses CSS 3.0 with browser specific enhancements.
</p>

<h2>Links</h2>

Wherever possible links are written to make sense out of context enabling browsers such as JAWS, Lynx or Opera to extract a list of links on a page. Wherever possible link text is never duplicated on a page.

<h2>Images</h2>

All content images used in this site include descriptive ALT attributes. Purely decorative graphics include null ALT attributes.

<h2>Visual Design</h2>

The site uses cascading style sheets for visual layout.

<h2>Javascript</h2>

In common with all web 2.0 applications, the use of Javascript on the Seastar Superbikes website is essential, disabling Javascript within your browser will profoundly affect your use of the website. 
This website has been tested in a variety of browsers including Safari, Chrome, Internet Explorer, Firefox and Opera.

<h2>Accessibility Queries</h2>
<p>
If you have any difficulties using this site that you believe we could address, please let us know. It would be helpful if your e-mail contained the following:

    The URL(s) (web-address) of the page(s) that you are having difficulties with
    The nature of your disability
    A description of the problem area
    If you have a solution to suggest, please feel free to do so.
</p>